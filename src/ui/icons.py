def get_icon(name, color=None, size=24):
    """
    Generate a QIcon from the internal SVG library.
    
    Args:
        name (str): Key in ICONS dict.
        color (str): Hex color (e.g., "#FFFFFF"). If None, uses default.
        size (int): Size of the pixmap.
        
    Returns:
        QIcon: The generated icon.
    """
    svg_data = ICONS.get(name)
    if not svg_data:
        return QIcon()
        
    # Inject color if provided
    if color:
        # Simple string replacement for color. SVGs use "currentColor" usually.
        # We replace stroke="currentColor" with stroke="color"
        svg_data = svg_data.replace("currentColor", color)
    
    renderer = QSvgRenderer(QByteArray(svg_data.encode('utf-8')))
    pixmap = QPixmap(size, size)
    pixmap.fill(QColor(0, 0, 0, 0)) # Transparent
    
    painter = QPainter(pixmap)
    renderer.render(painter)
    painter.end()
    
    return QIcon(pixmap)

def get_pixmap(name, color=None, size=24):
    """Return QPixmap directly."""
    icon = get_icon(name, color, size)
    return icon.pixmap(size, size)
